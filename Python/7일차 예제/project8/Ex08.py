import numpy as np

ar = np.array([[1, 1], [2, 2]])
print("ar  합 : ", ar.sum())
print("ar x축 합 : ", ar.sum(axis=0))
print("ar y축 합 : ", ar.sum(axis=1))
print()
# 배열과 숫자의 연산
print(ar*4)
print()
print(ar*1.5)
print()
